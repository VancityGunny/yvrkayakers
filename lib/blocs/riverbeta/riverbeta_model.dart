  }
  Map<String, dynamic> toJson() {
    final data = <String, dynamic>{};
    data['entries'] = entries.map((e) => e.toJson()).toList();
    data['visitors'] = visitors.map((e) => e.toJson()).toList();
    return data;
  }
}

